package calculatorLv2;

public class Operation {
    public int add(int x,int y){ // 더하기 메소드
        return x+y;
    }
    public int subtract(int x,int y){ //빼기 메소드
        return x-y;
    }
    public int multiply(int x, int y){ // 곱하기메소드
        return x*y;
    }
    public int divide(int x, int y){ // 나누기 메소드
        return x/y;
    }
}
